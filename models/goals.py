class Goal:
    def __init__(self, goal_id, date, game_id, minute, club_name, player_name, description):
        self.goal_id = goal_id
        self.date = date
        self.game_id = game_id
        self.minute = minute
        self.club_name = club_name
        self.player_name = player_name
        self.description = description
